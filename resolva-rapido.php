    	<h2>precisa de alvará dos bombeiros</h2>
        <p>Uma alternativa para resolver seus problemas com alvará é a realização de um Projeto de Prevenção 
e proteção Contra Incêndio, ou seja, planejar e projetar todos os sistemas que podem auxiliar você e 
seus vizinhos a se protegerem em caso de incêndio.</p>
		<p class="sub-titulo">O que é o PPCI?</p>
		<p>O PPCI é o Plano de Prevenção e proteção Contra Incêndio que é aprovado pelo Corpo de Bombeiros 
(CB) atestando que a edificação possui sistemas de segurança suficientes para controlar algum incên-
dio até que o CB chegue no local para combater o mesmo.</p>


		<p class="sub-titulo">Como ele Funciona?</p>
        <p>O PPCI serve para controlar alguma situação de risco envolvendo fogo até que o CB tenha acesso ao 
local para fazer o salvamento das pessoas e posterior inibição total do incêndio. Além de garantir que 
há o número mínimo de pessoas treinadas para agir em situação de risco.</p>
        
        
        <p class="sub-titulo">Vantagens de se regularizar</p>
		<p>No caso de ocorrer um incêndio, o alvará garante que o edifício possui todos os sistemas possíveis 
de controlar o fogo, ou seja, as pessoas que estão nele ou próxima dele não correm riscos exagerados.</p>

		<p class="sub-titulo">O que compõe o projeto?</p>
        <p>Nele contém todos os dimensionamentos de hidrantes, extintores, sinalização, iluminação, rotas de 
fuga por exemplo. E outros sistemas exigidos pelo CB que dependem de cada edificação.</p>

