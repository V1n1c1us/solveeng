        <h2>ESTÁ COM INFILTRAÇÃO/VAZAMENTO OU RACHADURA EM CASA?</h2>
        <p>Quando se quer apenas registrar fotograficamente e com um documento de um responsável técnico a nossa 
sugestão é fazer uma vistoria.</p>
		<p><span class="sub-titulo">O que é uma vistoria?</span></p>
        <p>Uma vistoria técnica é uma constatação visual de algum fato ou acontecimento na edificação. Sem a utilização 
de equipamentos para investigar, apenas para fotografar e registrar o ocorrido.</p>
		<p><span class="sub-titulo">Como ela funciona?</span></p>
		<p>O profissional vai até o local, analisa os acontecimentos e suas respectivas gravidades e registra de forma foto-
gráfica e com um relatório técnico. Esta vistoria, para garantir a segurança e compromisso do técnico com o 
cliente, é gerada uma Anotação de Responsabilidade Técnica (ART) que garante a responsabilidade técnica 
de quem fez a análise e emitiu o relatório técnico.</p>
		<hr>
        <p>Quando se quer investigar a origem de um fato ou acontecimento e ter um laudo técnico imparcial atestando 
a realidade dos fatos de um responsável técnico a nossa sugestão é fazer uma <span class="sub-titulo">perícia</span>.</p>
        <p class="sub-titulo">O que é uma perícia?</p>
        <p>A perícia técnica serve para apurar a origem, as causas e mecanismos de ação que geraram o acontecimento. Neste caso há a utilização de equipamentos necessários para que se encontre a origem e até mesmo a parti-
cipação de outros profissionais especialistas em cada caso.</p>
		<p class="sub-titulo">Como ela funciona?</p>
        <p>A perícia é realizada de forma imparcial pelos técnicos a fim de se encontrar a causa de um acontecimento, registrar o mesmo e emitir um documento atestando as conclusões dos técnicos.
Uma perícia não é somente judicial, se chama assim toda e qualquer investigação em alguma acontecimento, seja uma rachadura ou uma infiltração.</p>
		<p>Esta perícia, para garantir a segurança e compromisso do técnico com o cliente, é gerada uma Anotação de Responsabilidade Técnica (ART) que garante a responsabilidade técnica de quem fez a análise e emitiu o laudo técnico.</p>
        <hr>
        <p>Quando se quer saber a solução/concerto para um fato ou acontecimento e ter um parecer técnico imparcial atestando a realidade dos fatos e como solucioná-los por um responsável técnico a nossa sugestão é fazer uma <span class="sub-titulo">consultoria</span>.</p>
        <p class="sub-titulo">O que é uma consultoria?</p>
        <p>Uma consultoria vai mais além que uma perícia. Ela investiga, encontra a causa e ainda fornece maneiras de como proceder para arrumar o problema, ou seja, diz exatamente como arrumar o acontecimento para que ele não volte a acontecer e ainda há o acompanhamento do responsável técnico no processo.</p>
