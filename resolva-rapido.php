    	<h2>PROJETOS DE SISTEMA FOTOVOLTAICO</h2>
        
        <p>Uma forma de exercer seu direito de consumidor é de recorrer ao fabricante sobre os problemas no 
produto comprado. Uma edificação também é um produto, nesse caso, e o fabricante é a construtora. Sendo assim, segundo o Código do Consumidor, a construtora tem responsabilidade total sobre a edificação durante os 05 (cinco) primeiros anos desta. Por isso, nossa sugestão é uma <span class="sub-titulo">vistoria de edificação em garantia</span>.</p>
        
        <p><span class="sub-titulo">O que é?</span></p>
        <p>A vistoria de edificações em garantia serve como suporte para a construtora/incorporadora ou para o síndico. Ela auxilia no controle do desempenho da edificação, como por exemplo, se a pintura está com as condições adequadas, se há vazamento em alguma instalação ou aparecimento de fissuras, entre outras possíveis falhas que podem acontecer em um imóvel novo. Facilitando o reparo pela parte da construtora.</p>                
        
        <p><span class="sub-titulo">Como Funciona?</span></p>                
        <p>Este trabalho funciona de forma a facilitar o trabalho das construtoras/incorporadoras e de síndicos no controle dos acontecimentos internos na edificação. Apontando-se as falhas mais importantes e os reparos mais urgentes.</p>
        
        <p><span class="sub-titulo">Vantagens?</span></p>
        <p>Com este serviço, a construtora evita um gasto excessivo com reparos futuros na maneira corretiva (corrigir após o estrago). Se a empresa se preocupar com os acontecimentos de forma preventiva, com certeza evitará desgaste de relações síndico-proprietário-construtora e ainda terá o controle do 
desempenho dos seus empreendimentos, podendo valorizar ainda mais as propostas da empresa.</p>                
               
        
