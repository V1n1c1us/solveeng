    	<h2>PROBLEMAS EM SUA INSTALAÇÃO ELÉTRICA?</h2>
	       
         <p>Uma alternativa para resolver o problema em sua instalação elétrica é realizar uma avaliação completa instalação elétrica e identificar possíveis problemas e propor soluções viáveis para a correção deles.</p>  
                
    	<p>Esta avaliação consiste na inspeção visual de toda a instalação elétrica e a utilização de equipamentos de análise de energia elétrica. Com estas duas ferramentas é possível verificar os problemas e propor soluções para que as instalações estejam de acordo com as normas vigentes e em perfeito funcionamento.</p>
                
        <p><span class="sub-titulo">Por que fazer?</span></p>
        <p>Muitas instalações elétricas foram projetadas e feitas em épocas onde aparelhos como o condicionador de ar, forno micro-ondas, forno elétrico, chuveiros eletrônicos e torneiras elétricas nem existiam. Com o surgimento destes equipamentos, a instalação elétrica passou a fornecer maiores índices de energia e não sofreu as adequações mínimas necessárias em questões de segurança e confiabilidade. Sendo assim, a utilização destes equipamentos em instalações antigas causa diversos problemas como sobrecargas, pontos de aquecimento, impossibilidade de utilização mais eficiente da instalação e outros vários fatores que poderiam ser citados.</p>
        
        <p><span class="sub-titulo">Características de uma instalação elétricas com problemas:</span></p>
        
        <p><span class="sub-titulo">Inexistência de sistema de aterramentos: </span>Por norma, todas as edificações devem possuir um sistema 
de aterramento que será responsável por descarregar para a terra toda a energia que pode causar danos a pessoas e animais. Sem este sistema os fabricantes não emitem a garantia de equipamentos danificados por descargas atmosféricas, por exemplo.</p>
        <p><span class="sub-titulo">Grandes perdas de energia:</span> As perdas de energia, e consequente pagamento por essas perdas, são 
geradas pela utilização de fios que não estão dimensionados corretamente para tal função e já estão com muito tempo de utilização</p>
        <p><span class="sub-titulo">Falta de segurança:</span> Instalações elétricas antigas comumente não recebem manutenções e tem como característica a criação de pontos de aquecimento em todo a sua extensão, grande quantidade de emendas e redução da isolação de tomadas e condutores, podendo causar choques ou curto-circuito para quem venha a tocar nestes componentes.</p>
        <p><span class="sub-titulo">Impossibilidade de emissão de laudos:</span> A cada dois anos é necessário renovar o alvará dos Bombeiros e neste processo é fundamental a emissão de laudo das instalações elétricas. Se a instalação não estiver de acordo com as normas vigentes, fica tecnicamente inviável a emissão de um laudo elétrico.</p>
        
