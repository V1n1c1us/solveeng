        <p class="sub-titulo">O que é o projeto fotovoltaico?</p>
        <p>O projeto fotovoltaico consiste no dimensionamento de painéis solares, inversores, fios, disjuntores 
e demais equipamentos necessários para o perfeito funcionamento do sistema.</p>
        
        <p class="sub-titulo">Como funciona?</p>
        <p>O sistema de geração de energia fotovoltaica consiste, basicamente, na geração de energia elétrica através da utilização de placas de silício, onde estas placas recebem luz solar e realizam a conversão da energia solar em energia elétrica.  A energia elétrica gerada por estas placas alimenta televisões, geladeiras, chuveiro, condicionadores de ar, entre outros. Desta forma, o consumo mensal de energia é reduzido, resultando em economia de energia, e por consequência, redução dos gastos com energia elétrica.</p>
        
        <p class="sub-titulo">Vantagens?</p>
       		<ul class="margin-lista">
        		<li>Custo zero de geração de energia: visto que a radiação solar é abundante e de graça;
				<li>Custo zero de manutenção do sistema: o sistema funciona de forma automática e a limpeza dos painéis é realizada pela própria água da chuva;</li>
				<li>Possibilidade de instalação no telhado existente: os painéis são revestidos com vidro temperado de 3.2 mm, o que os protege compra chuvas de granizo;</li>
				<li>Excedente de energia gerada podem ser repassados para outra conta de energia (desde que aten-
	didos pela mesma concessionária): os créditos de energia podem ser consumidos em até 5 anos;</li>
				<li>Valorização do imóvel em até 15%*;</li>
				<li>Zero de emissão de poluentes;</li>
				<li>Acompanhamento em tempo real da geração de energia através de aplicativo para celular ou em site;</li>
				<li>Vida útil superior a 25 anos e com eficiência mínima de 80% do sistema, garantida pelos fabricantes;</li>
				<li>Todos os equipamentos utilizados possuem certificação do INMETRO.</li>                           
