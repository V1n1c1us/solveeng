		<p><span class="sub-titulo">O que é?</span></p>
        <p>A auditoria técnica de projeto serve para conferir se o projeto comprado foi executado conforme o 
planejado. Garantindo a excelência da execução e a satisfação final do nosso cliente.</p>
                
		<p><span class="sub-titulo">Como Funciona?</span></p>
