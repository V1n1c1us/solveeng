a respeito das manutenções necessárias para seus imóveis, através da elaboração do plano de manutenção do edifício, tudo isso visando a valorização e conservação adequada do imóvel.</p>
		<p>A inspeção predial é um conjunto de exames feitos por profissional habilitado a fim de verificar a capacidade funcional da edificação, ou seja, apontar as suas condições técnicas, funcionais e de conservação, a fim de determinar as medidas preventivas e corretivas para a boa vida do imóvel.</p>
