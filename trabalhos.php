        <p>As edificações são projetadas e pensadas para durarem um certo período de tempo que chamamos de 
Vida útil. Este período pode ser aproveitado na sua totalidade ou pode ser reduzido drasticamente se não houver cuidados específicos com a edificação. Além de reduzir a vida útil, a falta de cuidados com os imó-veis podem colocar em risco a vida de seus usuários, bem como depreciar o bem.</p>
		<p>A <span class="sub-titulo">SOLVE ENGENHARIA & CONSULTORIA</span> conta com equipe profissional capacitada para realizar inspeções prediais a fim de orientar seus clientes da melhor forma para providenciar os reparos e ações conforme as reais necessidades do imóvel.</p>
